package class7;

public class ForLoops {
    public static void main(String[] args) {

        for (int a = 0; a <=9; a++) {
            System.out.print(a + " ");
        }
        System.out.println(" ");
        int b = 0;
        while (b < 10) {
            System.out.print(b + " ");
            b++;
        }
    }
}
