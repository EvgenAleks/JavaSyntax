package class3;

public class IfElse_9 {
    public static void main(String[] args) {

        char c = 'M';
        char d = 'F';

        if (c == 'M') {
            System.out.println("Male");
        }

        if (d == 'F') {
            System.out.println("Female");
        }

        String name = "Tom";

        if (name.equals("Sam")) {
            System.out.println("Good student");
        }
        if (!name.equals("Sam")) {
            System.out.println("Very good student");
        }
    }
}
